#include <stdio.h>
#include <locale.h>

// Faça um programa que apresente o resultado da soma e média aritmética dos números situados na faixa de 50 a 70.

int main () {
	
	setlocale (LC_ALL, "Portuguese");
	
	float soma = 0, media = 0;
	
	for (int n = 50; n <= 70; n++) {
		
		soma = soma + n;
	}
	
	media = soma/20;
	
	printf ("Soma: %.0f \n", soma);
	printf ("Média: %.0f \n", media);
	
	return 0;
}
